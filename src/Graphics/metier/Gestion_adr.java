    public void inject_adr() {
        try {
            afl = adresseDAO.aff();
            if (adr != null) {
                adr.removeAllItems();
            }

            for (int i = 0; i < afl.size(); i++) {
                dlmadr.addElement(afl.get(i).toString());
            }
            adr.setModel(dlmadr);

        } catch (SQLException e) {
            System.out.println("Exception" + e);
        }
    }

