    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try {
            int ida = adr.getSelectedIndex();
            Adresse a = afl.get(ida);
            adresseDAO.delete(a);
            inject_adr();
            JOptionPane.showMessageDialog(this, "Adresse Supprimer", "Succès", JOptionPane.INFORMATION_MESSAGE);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "ERREUR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            int ida = adr.getSelectedIndex();
            Adresse a = afl.get(ida);
            String locat = loc.getText();
            int code = Integer.parseInt(cp.getText());
            String r = rue.getText();
            String n = num.getText();
            adresseDAO.update(new Adresse(a.getIdadr(), code, locat, r, n));
            inject_adr();
            JOptionPane.showMessageDialog(this, "Adresse mis à jour", "Succès", JOptionPane.INFORMATION_MESSAGE);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "ERREUR", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            afl = adresseDAO.aff();
            int nr = dft1.getRowCount();
            for (int i = nr - 1; i >= 0; i--) {
                dft1.removeRow(i);
            }
            for (Adresse a : afl) {
                Vector v = new Vector();
                v.add(a.getIdadr());
                v.add(a.getNum());
                v.add(a.getRue());
                v.add(a.getLocalite());
                v.add(a.getCp());
                dft1.addRow(v);
                
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "ERREUR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed


